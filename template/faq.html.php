            <h3 class="question2">Q2. Can I use a personal music device while cycling?</h3>
        
            <p class="answer2">Absolutely no music devices are allowed during the bike segment for obvious safety risks and will result in immediate disqualification.</p>
            
        </section>
        
        <section class="faqs-box">
        
            <h3 class="question3">Q3. Can I use a personal music device while running?</h3>
        
            <p class="answer3">During any running segments, we prefer for athletes to NOT use music devices to maximize safety, assure a fair competitive environment and assure athletes can hear all course marshal instructions. With that said, we also understand that many athletes rely on music to help endure the challenge of running. To accommodate the needs of our athletes while still assuring maximum safety and a fair, competitive environment, here is the arrangement that we can accommodate. If you chose to use a personal music device, you must always keep one ear open for instructions so only one ear-bud can be used at any time. In addition, if you opt to use music, you will not be eligible for awards, points and rankings. So as an athlete, you have to make a choice…music or awards/points/rankings.<br>
            <br>
            If you are using a personal music devise with two earbuds, you will be immediately disqualified. Trust us, we don’t want to have to enforce a disqualification but in order to assure the safety of all athletes, this is a very important rule.<br>
            We also request that if you chose to use a personal music device that you keep the volume to a minimum to assure all instructions can be heard.
            </p>
            
        </section>
        
        <section class="faqs-box">
        
            <h3 class="question4">Q4. Do I need to wear a wetsuit?</h3>
        
            <p class="answer4">No, you do not need to wear a wetsuit. Many will not wear a wetsuit while others will choose to wear a wetsuit because of the buoyancy and warmth factor. </p>
            
        </section>
        
        <section class="faqs-box">
        
            <h3 class="question5">Q5. Do I have to use a road or racing bike?</h3>
        
            <p class="answer5">No. We welcome any type of bike as long as it is functioning properly with brakes and endcaps at the end of your handlebars.</p>
            
        </section>
            
